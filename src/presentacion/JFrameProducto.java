    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        // TODO add your handling code here:
        if (validarCampos("TODOSLOSCAMPOS")) {
            int id = Integer.parseInt(jTextFieldId.getText());
            int valor_base = Integer.parseInt(jTextFieldCantidad.getText());
            String nombre = jTextFieldNombre.getText();
            String temperatura = jComboBoxCategoria.getSelectedItem().toString();
            double costo = Double.parseDouble(jTextFieldPrecio.getText());
            Producto p = new Producto();
            p.setId(id);
            p.setNombre(nombre);
            p.setValor_base(valor_base);
            p.setCosto(costo);
            p.setTemperatura(temperatura);
            if (p.guardarProducto()) {
                JOptionPane.showMessageDialog(this, "Guardado");
            } else {
                JOptionPane.showMessageDialog(this, "No guardado");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Verificar valores");
        }
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

