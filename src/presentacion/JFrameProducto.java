    private void jButtonConsultarPorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarPorIdActionPerformed
        // TODO add your handling code here:
        if (validarCampos("SOLOID")) {
            int id = Integer.parseInt(jTextFieldId.getText());
            Producto p = new Producto();
            p = p.getProducto(id);
            jTextFieldNombre.setText(p.getNombre());
            jTextFieldCantidad.setText(Integer.toString(p.getValor_base()));
            jTextFieldPrecio.setText(Double.toString(p.getCosto()));
            try {
                jComboBoxCategoria.setSelectedItem(ItemsCategoria.valueOf(p.getTemperatura()));
            } catch (NullPointerException e) {
            }
        } else {
            JOptionPane.showMessageDialog(this, "Validar el campo id");
        }

    }//GEN-LAST:event_jButtonConsultarPorIdActionPerformed

    public boolean validarCampos(String proceso) {
        String valor_base = jTextFieldCantidad.getText();
        String nombre = jTextFieldNombre.getText();
        String costo = jTextFieldPrecio.getText();
        String id = jTextFieldId.getText();
        boolean respuesta = false;
        switch (proceso) {
            case "SOLOID":
                if (isNumero(id)) {
                    respuesta = true;
                }
                break;
            case "TODOSLOSCAMPOS":
                if (isNumero(id) && isNumero(valor_base) && isNumero(costo) && !nombre.isEmpty()) {
                    respuesta = true;
                }
                break;
            default:
                break;
        }
        return respuesta;
    }

    public boolean isNumero(String cadena) {
        boolean bandera = false;
        try {
            double a = Double.parseDouble(cadena);
            bandera = true;
        } catch (NumberFormatException ex) {
        }
        return bandera;
    }

